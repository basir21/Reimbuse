package entity;

public class Keterangan {
	String idKeterangan;
	double parkir;
	double bpjs;
	double taxi;
	double transport;
	
	
	public String getIdKeterangan() {
		return idKeterangan;
	}
	public void setIdKeterangan(String idKeterangan) {
		this.idKeterangan = idKeterangan;
	}
	public double getParkir() {
		return parkir;
	}
	public void setParkir(double parkir) {
		this.parkir = parkir;
	}
	public double getBpjs() {
		return bpjs;
	}
	public void setBpjs(double bpjs) {
		this.bpjs = bpjs;
	}
	public double getTaxi() {
		return taxi;
	}
	public void setTaxi(double taxi) {
		this.taxi = taxi;
	}
	public double getTransport() {
		return transport;
	}
	public void setTransport(double transport) {
		this.transport = transport;
	}
	public String getIdTipeKlaim() {
		return idKeterangan;
	}
	public void setIdTipeKlaim(String idTipeKlaim) {
		this.idKeterangan = idTipeKlaim;
	}
	
}
